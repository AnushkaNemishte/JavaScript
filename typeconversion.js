//Implicit conversion occurs when JavaScript automatically converts one data type to another without any explicit instructions from the programmer.
//Explicit conversion occurs when the programmer manually converts one data type to another using built-in functions or type conversion methods.
//Implicit example
console.log(true + "3");
console.log("Bruce" - "wayne");
console.log("5" - null);
console.log(5 + undefined);

//Explicite
console.log(Number('10'))
console.log(parseInt('25'));
console.log(parseFloat('8.55'));
console.log(String(78));